package Dades;

public class Endoll {
    private int id;
    float potencia;

    public Endoll(int id, float potencia) {
        this.id = id;
        this.potencia = potencia;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public float getPotencia() {
        return potencia;
    }

    public void setPotencia(int potencia) {
        this.potencia = potencia;
    }
}
